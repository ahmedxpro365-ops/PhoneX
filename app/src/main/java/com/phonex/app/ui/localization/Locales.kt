    fun get(key: String, lang: String): String {
        return if (lang == "Arabic") {
            ar[key] ?: en[key] ?: key
        } else {
            en[key] ?: key
        }
    }
}

@Composable
fun stringLoc(key: String, viewModel: PhoneViewModel): String {
    val langState = viewModel.selectedLanguage.collectAsState()
    return Locales.get(key, langState.value)
}
